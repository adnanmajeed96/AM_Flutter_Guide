void main()
{
  print("loop tutorial:");

  // for loop work on intialize,condition,increment or decrement
  int count=0;
  for(int i=0;i<10;i++)
    {
      print("count:$count");
      count++;
    }


  //do while loop but (condition is false so do-while run always run one time)
  int no=100;
  do{
    print("no not greater than 100 do ");

  }while(no<100);

}